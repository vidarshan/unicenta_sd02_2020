    public void arithmetic_operations(){
        switch(calculation){
            case 1:
                answer = num + Double.parseDouble(ans.getText());
                ans.setText(Double.toString(answer));
                break;
            case 2:
                answer = num - Double.parseDouble(ans.getText());
                ans.setText(Double.toString(answer));
                break;
            case 3:
                answer = num * Double.parseDouble(ans.getText());
                ans.setText(Double.toString(answer));
                break;
            case 4:
                answer = num / Double.parseDouble(ans.getText());
                ans.setText(Double.toString(answer));
                break;
        }
    }
